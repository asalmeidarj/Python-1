""" Arredondar número utilizando função intera round """                  


def arredondar_valor():
    primeiro_num = 1.23456789  # número aleatório 
    segundo_num = 3  # quantidade de algarismos para arredondar
    print(round(primeiro_num, segundo_num)) 


arredondar_valor() 

